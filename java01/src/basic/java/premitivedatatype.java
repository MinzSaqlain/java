package basic.java;

public class premitivedatatype {
	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static char ch;
	static boolean bb;
	
	
	public static void main(String[] args) {
		System.out.println(premitivedatatype.b); //0
		System.out.println(premitivedatatype.s); //0
		System.out.println(premitivedatatype.i); //0
		System.out.println(premitivedatatype.l);//0
		System.out.println(premitivedatatype.f);//0.0
		System.out.println(premitivedatatype.d);//0.0
		System.out.println(premitivedatatype.ch);
		System.out.println(premitivedatatype.bb);// false 
		//primitive data types shows all the default value 
		
	}
}
